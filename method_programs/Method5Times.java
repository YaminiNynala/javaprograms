class Method5Times 
{
	public static void typeMessage()
	{
		System.out.println("chat with mom");
	}
	public static void main(String[] args) 
	{
		System.out.println("main starts...");
		typeMessage();
		typeMessage();
		typeMessage();
		typeMessage();
		typeMessage();
		System.out.println("main ends...");
	}
}
